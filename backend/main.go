type RegisterRequest struct {
	ShadowID string `json:"shadow_id"`
}

func handleMobileRegister(c *fiber.Ctx) error {
	var req RegisterRequest
	if err := c.BodyParser(&req); err != nil || req.ShadowID == "" {
		return c.Status(400).JSON(fiber.Map{"error": "invalid format, expected shadow_id"})
	}

	// Try to find existing
	var existing Device
	if err := db.Where("shadow_id = ?", req.ShadowID).First(&existing).Error; err == nil {
		return c.JSON(fiber.Map{"token": existing.Token, "status": existing.Status})
	}

	// Create new
	newToken := uuid.New().String()
	newDev := Device{
		ShadowID: req.ShadowID,
		Token:    newToken,
		Status:   "created",
		CreatedAt: time.Now(),
	}
	if err := db.Create(&newDev).Error; err != nil {
		return c.Status(500).JSON(fiber.Map{"error": "db error"})
	}

	return c.JSON(fiber.Map{"token": newToken, "status": "created"})
}

func handleMobileLogin(c *fiber.Ctx) error {
	var req struct {
		Token string `json:"token"`
	}
	if err := c.BodyParser(&req); err != nil {
		return c.Status(400).JSON(fiber.Map{"error": "bad request"})
	}

	var dev Device
	if err := db.Where("token = ?", req.Token).First(&dev).Error; err != nil {
		return c.Status(401).JSON(fiber.Map{"error": "unauthorized"})
	}

	return c.JSON(fiber.Map{
		"shadow_id":  dev.ShadowID,
		"status":     dev.Status,
		"paid_until": dev.PaidUntil,
	})
}

func handleMobileGetServers(c *fiber.Ctx) error {
	auth := c.Get("Authorization")
	if auth == "" {
		return c.Status(401).JSON(fiber.Map{"error": "missing token"})
	}
	tokenStr := strings.TrimPrefix(auth, "Bearer ")
	
	var dev Device
	if err := db.Where("token = ?", tokenStr).First(&dev).Error; err != nil {
		return c.Status(401).JSON(fiber.Map{"error": "invalid token"})
	}

	// Future: Return a list of servers to ping
	return c.JSON([]fiber.Map{
		{
			"id": "eu-ru-bypass-1",
			"ip": os.Getenv("SERVER_IP"),
			"protocol": "vless+reality+vision",
			"load": 24, // fake load for MVP
			"pub": os.Getenv("REALITY_PUBLIC_KEY"),
		},
	})
}

func handleMobileGetConfig(c *fiber.Ctx) error {
	auth := c.Get("Authorization")
	if auth == "" {
		return c.Status(401).JSON(fiber.Map{"error": "missing token"})
	}
	tokenStr := strings.TrimPrefix(auth, "Bearer ")
	
	var dev Device
	if err := db.Where("token = ?", tokenStr).First(&dev).Error; err != nil {
		return c.Status(401).JSON(fiber.Map{"error": "invalid token"})
	}

	// Check if payment active
	if dev.PaidUntil == nil || dev.PaidUntil.Before(time.Now()) {
		// MVP: We'll allow free tier or just 1 hour demo if expired
		// return c.Status(403).JSON(fiber.Map{"error": "subscription expired"})
	}

	// Generate a unique Xray key for this device if not exists
	var vpnUser User
	err := db.Where("status = ? AND tg_id = ?", "active", int64(dev.ID)).First(&vpnUser).Error
	if err != nil {
		// Create a new key binding `dev.ID` to `TgID` (repurposed field for now)
		newUUID := uuid.New().String()
		vpnUser = User{
			UUID:      newUUID,
			Status:    "active",
			TgID:      int64(dev.ID),
			CreatedAt: time.Now(),
		}
		db.Create(&vpnUser)
		xrayManager.AddUser(newUUID, "dev_"+newUUID[:6])
	}

	vlessURL := fmt.Sprintf(
		"vless://%s@%s:443?type=tcp&security=reality&pbk=%s&fp=chrome&sni=addons.mozilla.org&sid=%s&spx=%%2F&flow=xtls-rprx-vision#VPN-%s",
		vpnUser.UUID, os.Getenv("SERVER_IP"), os.Getenv("REALITY_PUBLIC_KEY"), os.Getenv("REALITY_SHORT_ID"), dev.ShadowID[:6],
	)

	return c.JSON(fiber.Map{
		"config_url": vlessURL,
		"protocol":   "vless_reality",
	})
}
